						$(function () {
						  // Ensure there is an active slide immediately (required so first click works)
						  function ensureActiveSlide() {
							var $all = $('.callbacks1 li');
							if ($all.filter('.rslides1_on_slide').length === 0 && $all.length) {
								$all.css({'opacity':'0','z-index':'1'});
								var $first = $all.first();
								$first.addClass('rslides1_on_slide').css({'opacity':'1','z-index':'2'});
							}
						  }
						  
						  // Enable navigation immediately - no waiting
						  function enableCustomNavigation() {
							// Remove existing handlers to prevent duplicates
							$('#slider-prev, #slider-next').off('click');
							
							// Custom navigation functionality
							$('#slider-prev').click(function(e) {
								e.preventDefault();
								e.stopPropagation();
								e.stopImmediatePropagation();
								
								ensureActiveSlide();
								// Find current active slide
								var $currentSlide = $('.callbacks1 li').filter('.rslides1_on_slide').first();
								var $prevSlide = $currentSlide.prev('li');
								
								// If no previous slide, go to last slide
								if ($prevSlide.length === 0) {
									$prevSlide = $('.callbacks1 li').last();
								}
								
								// Properly switch slides with CSS handling
								$currentSlide.removeClass('rslides1_on_slide').css({
									'opacity': '0',
									'z-index': '1'
								});
								
								$prevSlide.addClass('rslides1_on_slide').css({
									'opacity': '1',
									'z-index': '2'
								});
								
								// Trigger responsiveSlides events
								$('#slider4').trigger('rslides:after');
								return false;
							});
							
							$('#slider-next').click(function(e) {
								e.preventDefault();
								e.stopPropagation();
								e.stopImmediatePropagation();
								
								ensureActiveSlide();
								// Find current active slide
								var $currentSlide = $('.callbacks1 li').filter('.rslides1_on_slide').first();
								var $nextSlide = $currentSlide.next('li');
								
								// If no next slide, go to first slide
								if ($nextSlide.length === 0) {
									$nextSlide = $('.callbacks1 li').first();
								}
								
								// Properly switch slides with CSS handling
								$currentSlide.removeClass('rslides1_on_slide').css({
									'opacity': '0',
									'z-index': '1'
								});
								
								$nextSlide.addClass('rslides1_on_slide').css({
									'opacity': '1',
									'z-index': '2'
								});
								
								// Trigger responsiveSlides events
								$('#slider4').trigger('rslides:after');
								return false;
							});
						  }
						  
						  // Ensure there's an active slide on load, then enable navigation
						  ensureActiveSlide();
						  enableCustomNavigation();
						  
						  var slider = $("#slider4").responsiveSlides({
							auto: true,
							pager:false,
							nav:false,
							speed: 500,
							namespace: "callbacks",
							before: function () {
							  $('.events').append("<li>before event fired.</li>");
							},
							after: function () {
							  $('.events').append("<li>after event fired.</li>");
							  // Re-enable navigation after each transition to ensure it stays working
							  ensureActiveSlide();
							  enableCustomNavigation();
							}
						  });
						});
						
